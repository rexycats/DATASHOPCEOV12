// Current language state
// Default language: Dutch. localStorage overrides this in the boot script.
let LANG = 'nl';

function t(key) {
  return (TRANSLATIONS[LANG] && TRANSLATIONS[LANG][key]) || (TRANSLATIONS['nl'][key]) || key;
}

function setLang(lang) {
  LANG = lang;
  try { localStorage.setItem('datashop_lang', lang); } catch(e) {}
  // Bug #2 fix: re-translate scenario fields when language changes
  if (typeof applyNarrativeTranslations === 'function') applyNarrativeTranslations();
  // I18N-3 fix: re-translate OFFICES, RANKS, ACHIEVEMENTS
  if (typeof applyGameDataTranslations === 'function') applyGameDataTranslations();
  applyTranslations();
  // BUG-3 fix: invalidate cached DOM references after i18n rebuilds DOM
  if (typeof EL !== 'undefined' && EL._flush) EL._flush();
  // NEW-BUG-3 fix: invalidate cached DateTimeFormat so timestamps use new locale
  if (typeof UI !== 'undefined' && UI._timeFmt !== undefined) UI._timeFmt = null;
  // Re-render dynamic panels if game is running
  if (document.getElementById('s-game')?.classList.contains('active')) {
    UI.renderSidebar?.();
    UI.renderScenarios?.();
    UI.renderAch?.();
    const activePanel = document.querySelector('.panel.on');
    if (activePanel) {
      const panelId = activePanel.id.replace('panel-','');
      UI.showPanel?.(panelId);
    }
  }
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const attr = el.getAttribute('data-i18n-attr');
    if (attr) {
      el.setAttribute(attr, t(key));
    } else {
      el.textContent = t(key);
    }
  });
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    if (typeof setHTML === 'function') {
      setHTML(el, t(key));
    } else {
      el.textContent = t(key);
    }
  });
  // Update lang switcher buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === LANG);
  });
  // Update html lang attr
  document.documentElement.lang = LANG === 'en' ? 'en' : 'nl';
}
